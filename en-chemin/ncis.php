					<p><strong>Responsable :</strong><em><br>
<br style="display: block; margin: 4px 0;"><a name="simulation-gibbs"></a><u>Maître Gibbs dit le linxe</u></em> (<a href="linxe.html" target="_blank" class="external">article parut dans l'Express</a>)<br>
					Le linxe est un animal bipède, ou presque, originaire du sud ouest de la Gaule, de la lignée des félins.<br> 
Descendants du Lynx d'Issoire, les lynx ont connu de nombreuses classifications taxinomiques différentes et les diverses espèces ont tour à tour été sous-espèces puis espèces à part entière.<br>
Cependant, Maître Gibbs se distingue par une pilosité capillaire particulière qui a inspiré un célèbre héros de série télévisée où figure notamment l’énervant Pinozzo qui se la pète en permanence.<br>
Parmi les caractéristiques moins visibles, le linxe ne possède que 28 dents, au lieu des 30 dents habituelles chez les félins.<br>
Sa migration dans l’agglomération parisienne remonte aux années 90, plus précisément à l’arrivée du Beaujolais nouveau. Cela reste une énigme pour les scientifiques.<br>
A l’instar de <a href="#simulation-max-la-menace">Max</a> dont l’odorat peut détecter des vapeurs de bière à plus de 200 mètres, Maître Gibbs est doté d’une acuité visuelle remarquable (confirmée par son ophtalmologue préférée) lui permettant de repérer une biche bien galbée même dans l’obscurité.<br>
Le linxe s’alimente principalement de ricard accompagné parfois de cacahuètes en période hivernal et de tapas au printemps dans les grandes occasions.<br>
On peut souligner son caractère altruiste pourtant peu répandu chez ses congénères. En effet il est capable de sacrifier un peu de son indépendance pour conduire une jeune autostoppeuse égarée sur l’autoroute allant même jusqu’à "discuter" avec elle...<br>
Sa période d’accouplement, de 22h00 à 3h00, est précédée généralement d’une phase de séduction qui consiste à procurer à sa partenaire un sentiment de confiance voire d’insouciance. Cette technique d’approche très élaborée est l’objet de nombreuses études car son efficacité peut conduire à une surpopulation de l’espèce dans les 40 ans à venir.<br>
Sa nomination au poste de directeur du département simulation du NCIS ne doit donc rien au hasard. Il a été démontré que la gestion d’une équipe comme celle-là nécessitait une implication physiologique telle (surtout avec <a href="#simulation-jose">José del Rio</a>), qu'elle est censée diminuer d’autant ses ardeurs sexuelles au coucher du soleil. Pour l’instant, les effets n’ont pu être vérifiés. Malheureusement des riverains auraient aperçu l’animal, chassant en meute dans le quartier Saint-Michel. En attendant une éventuelle confirmation, les autorités tentent de rassurer la population.<br>
