                        <a name="weekend"></a><h2>Un  
                           week-end en Bretagne Nord</h2>
                     
                        <p>Une matinée douce de septembre,  
                           banlieue parisienne. Chargement de la clio, sans oublier la 
                           <a href="../sensations/windsurf-kitesurf.php">jp</a>, la 
                           <a href="../sensations/windsurf-kitesurf.php">v6</a>, la  
                           tente, le matelas pneumatique. Direction la Bretagne nord. Plus précisément  
                           une presqu’île nichée au cœur de la Côte d’Émeraude. Le soleil et  
                           le vent de nord devraient nous accompagner pendant ces 4 jours de pèlerinage  
                           en terres Celtes. Le Mans, Rennes, Ploubalay, Sain-Jacut de la Mer, camping  
                           de la Manchette. Il n’y a pas foule. Cool on peut installer notre  
                           campement devant l‘accès à la plage. Pour l’instant, c’est une  
                           plaine de sable qui sommeille. La marée haute est prévue pour 14h. Les  
                           peupliers impatients s’agitent dans tout les sens. Un peu comme moi  
                           (j’ai pas navigué depuis 1 mois). Wish, mat, voile sous le bras,  
                           j’emprunte le sentier de la Pointe de la Justice pour gagner le spot…  
                           Magique, la baie se dévoile entièrement, lumineuse, offrant rochers et  
                           grèves de sables blancs à la Manche qui avance. J’adore ce moment.  
                           Prendre le temps de gréer quand les éléments se sont donnés  
                           rendez-vous. Le périphérique, internet, notre chef de l’état  
                           n’existent plus. Juste cette angoisse : le vent ne va-t-il pas se lasser  
                           ? Non, 15, 20 nœuds de nord soufflent régulièrement dans un ciel  
                           made in Cote d‘Azur. La mer arrive. Retour à la tente. Enfilage de  
                           combi (eh oui, latitudes nord tout de même), serrage de harnais et  
                           c’est parti au galop, planche sous le bras. Retour au spot. Ca va, la  
                           mer est là (pas de plantage dans le tableau des marées). Le vent ? OK. Décollage !
                        </p>
