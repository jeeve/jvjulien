			<h2>Dans  
              un bar de Malakoff</h2>
		 
            
			  <p>
              Hier soir, je discutais avec Ix-88-st un extra-terrestre, de la  
              galaxie M31 je crois.<br>  
              C'était dans un troquet de Malakoff, près de Paris.  
			  </p>
              <p>Un dimanche soir, un peu  
              perdu, je trouvais auprès de cette créature une source de  
              réconfort.<br>
              D'autant que c'est la première fois que je rencontre un  
              extra-terrestre.</p>
              <p>On a bavardé un bon  
              moment, surtout lui d’ailleurs. Ou elle. J’ai cru comprendre  
              que son espèce ne se reproduisait pas sexuellement, du moins pas  
              au sens terrien du terme.</p>  
              <p>Ce qui m’a frappé, c’est  
              sa façon d’évoquer les dissensions entre les principaux  
              acteurs inter galactiques. Le contexte de crise n’est  
              apparemment pas spécifique à notre monde. J’étais loin d’imaginer  
              les enjeux entre le partage du savoir des espèces évoluées. Par  
              « évolué » mon ami entend la maîtrise des déplacements  
              extra-stellaires. Je lui expliquais que l’on n’en était pas  
              encore là, si ce n’est quelques excursions sur notre lune.</p>  
              <p>Ix-88-st me raconta la  
              concurrence entre les grandes bibliothèques, qui au lieu d’unir  
              leurs connaissances pour former une banque de savoir universelle,  
              restreignaient les sources aux espèces jugées aptes.</p>  
              <p>Il m’expliqua comment son  
              espèce, les Ixis, déployait une campagne d’information à  
              travers les galaxies du groupe local de la Vierge, et comment, il  
              se retrouvait aujourd’hui face à moi pour m’alerter de la  
              situation.</p>
              <p>Je lui demandais pourquoi  
              ils avaient jugé bon de prévenir les humains, espèce encore  
              très jeune au regard de la diversité des communautés spatiales.</p>  
              <p>C’est un heureux hasard,  
              me dit-il. Le trou noir à bord duquel je parcourais la Voie Lactée  
              s’est évaporé à proximité de l’étoile Soleil, ce qui m’a  
              permis de me balader dans le système solaire. Une petite planète  
              toute bleue fut une bonne occasion de faire une escale.</p>  
              <p>Ce qui m’intriguait, c’était  
              comment un demi décimètre cube de liquide pouvait voyager  
              ainsi dans l’espace.</p>  
              <p>Le liquide jaune et gazeux  
              que vous voyez devant vous, ce n’est pas complètement moi,  
              disons que c’est un habit pratique pour communiquer dans des  
              conditions de pesanteur non nulles. Puis-je me balader dans  
              votre corps, me demanda-t-il ? C’est la première fois que je  
              rencontre un être macro moléculaire.</p>  
              <p>Ca tombait bien, j’avais  
              encore soif. Et puis, allez, c’est la dernière de la soirée.  
              Je savourais donc cette ultime bière plus intensément que  
              jamais.</p>

