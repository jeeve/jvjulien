						<a name="claude-imbert"></a><h2>Symptômes épidermiques après lecture d'un 
			éditorial de 
			<a target="_blank" href="http://fr.wikipedia.org/wiki/Claude_Imbert_(journaliste)">Claude Imbert</a></h2>
			
			
			<p>Comme souvent quand je lis un éditorial de Claude 
			Imbert, je ne peux pas dormir. Du coup, j'écris. Claude Imbert est 
			un éditorialiste du Point. C'est un peu comme un peintre de paysages 
			politiques, de scènes économiques. Il livre sa sensibilité avec son 
			style, ses couleurs, marie nuances de tons, envolées lyriques, et 
			caricature. Claude Imbert est un artiste et j'aime son talent. C'est 
			pour cela qu'il m'arrive de contempler ses éditoriaux. On pourrait 
			presque dire que je partage ses idées ou plutôt ses constats car 
			monsieur Imbert est en fait un photographe. Il ne propose rien, il 
			observe. Ses clichés ont des tonalités froides, bleutés, un 
			caractère cynique et celui de ce matin particulièrement cru. La fin 
			des états providence. La fin du rose, la victoire par forfait du 
			bleu. Le tsunami de la mondialisation économique répand les effets 
			du dumping social jusqu'à nous, pauvres français. Monsieur Imbert 
			jubile, l'idéal socialiste gaulois n'est plus que ruines auxquelles 
			s'accrochent encore quelques nostalgiques. La gauche, l'exception 
			française, la bâtisseuse du modèle social, c'est du passé. Et la 
			droite alors ? La droite est moderne, décomplexée et assume la 
			responsabilité de sa logique financière dans la crise. Là, je ne 
			comprends plus monsieur Imbert. Comment peut-on applaudir les 
			démolisseurs ? Je ne comprends plus la politique alors même que je 
			commençais à m'y intéresser. C'est peut-être le fameux clivage gauche droite qui 
			est périmé. L'argent devient le moteur du monde, ce n'est plus 
			seulement le carburant. Le vrai conducteur n'est plus le politique, 
			c'est le consommateur. Le citoyen, c'était avant, dans l'autre 
			monde. Le pouvoir ne germe plus uniquement des urnes, il se construit au 
			quotidien, dans nos choix de consommateur, statut peu séduisant mais 
			d'une puissance considérable. Certains scandent "travailler plus 
			pour gagner plus". Moi ce soir, dans l'euphorie qui succède souvent 
			la lecture des éditoriaux de Claude Imbert, je proclame "consommer 
			moins pour consommer mieux". Merde au dumping social. Vive le 
			développement durable, quête du 21ème siècle pour humains en mal 
			d'idéal. D'accord docteur, je vais me coucher. </font>
			</p><p align="right"><em><font size="2">mercredi 9 juin 2010</font></em></p>
