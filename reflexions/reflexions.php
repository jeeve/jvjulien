						<h2>Le lampadaire</h2><p>L’hiver est tenace, la 
			neige virevolte autour du lampadaire. J’aime bien regarder la neige 
			s’inviter dans la ville. C’est marrant, on a l’impression qu’elle 
			tombe seulement sur le lampadaire, attirée par la lumière comme une 
			star. Ce phénomène m’interpelle sur la nature de nos interprétations 
			du monde. On croit ce que l’on voit. Mais ce que l’on ne voit pas 
			peut exister. Les flocons n’ont probablement pas choisi ce 
			magnifique lampadaire comme unique compagnon. Le monde est 
			certainement infiniment plus grand, plus riche, plus mystérieux que 
			pourrait l'espérer notre esprit. Le lampadaire une conscience, chaque 
			flocon un univers.</p>  
